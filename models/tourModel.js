//Query Middleware
//the find hook will look at the current query not the current document
tourSchema.pre(/^find/, function (next) {
  //^find will tring all strings that start with find.
  this.find({ secretTour: { $ne: true } });

  this.start = Date.now();
  next();
});

tourSchema.post(/^find/, function (docs, next) {
  console.log(`Query took ${Date.now() - this.start} ms`);
  console.log(docs);
  next();
});

const Tour = mongoose.model('Tour', tourSchema);

module.exports = Tour;
